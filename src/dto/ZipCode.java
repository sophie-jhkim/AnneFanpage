package dto;

public class ZipCode {
	private String zipcode;
	private String sido;
	private String gugun;
	private String dong;
	private String biulding;
	private String bungi;
	private String seq;
	public String getZipcode() {
		return zipcode;
	}
	public ZipCode setZipcode(String zipcode) {
		this.zipcode = zipcode;
		return this;
	}
	public String getSido() {
		return sido;
	}
	public ZipCode setSido(String sido) {
		this.sido = sido;
		return this;
	}
	public String getGugun() {
		return gugun;
	}
	public ZipCode setGugun(String gugun) {
		this.gugun = gugun;
		return this;
	}
	public String getDong() {
		return dong;
	}
	public ZipCode setDong(String dong) {
		this.dong = dong;
		return this;
	}
	public String getBiulding() {
		return biulding;
	}
	public ZipCode setBiulding(String biulding) {
		this.biulding = biulding;
		return this;
	}
	public String getBungi() {
		return bungi;
	}
	public ZipCode setBungi(String bungi) {
		this.bungi = bungi;
		return this;
	}
	public String getSeq() {
		return seq;
	}
	public ZipCode setSeq(String seq) {
		this.seq = seq;
		return this;
	}

}
